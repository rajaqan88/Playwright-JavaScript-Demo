//test.use({viewport:{width:1234,height:8787}})

test('ActTime_1', async({page})=>{

    const ele_int = new ele(page)

    await  ele_int.navigate("http://127.0.0.1/login.do")

    const common = new common_page(page)
    
    await common.loginToApplication("admin","manager")

    await page.waitForTimeout(2000)

    
})

